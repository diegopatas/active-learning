# Program calculates age
p_age = 80
if p_age <= 2:
    print("You are a baby.")
elif p_age < 4:
    print("You are a toddler.")
elif p_age < 13:
    print("You are a kid.")
elif p_age < 20:
    print("You are a teenager.")
elif p_age < 65:
    print("You are an adult.")
elif p_age >= 65:
    print("You are elder.")